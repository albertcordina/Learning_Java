public class NumericOperations {

	public static void main(String[] args) {

		int add1 = 5;
		double add2 = 6.5;
		double addResult = add1 + add2;
		
		System.out.println("The result of the addition is in 'double': " + addResult);
		
		
		float sub1 = 8;
		int sub2 = 3;
		float subResult = sub1 - sub2;
		
		System.out.println("The result of the subtraction is in 'float': " + subResult);
		
		
		short mult1 = 7;
		long mult2 = 4;
		long multResult = mult1 * mult2;
		
		System.out.println("The result of the multiplication is in 'long': " + multResult);
		
		
		double div1 = 12.8;
		double div2 = 3.2;
		double divResult = div1 / div2;
		
		System.out.println("The result of the division is in 'double': " + divResult);
	}
}
